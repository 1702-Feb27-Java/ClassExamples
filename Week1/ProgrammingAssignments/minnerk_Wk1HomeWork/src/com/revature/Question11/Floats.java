package com.revature.Question11;

public class Floats {
	private float number1 = 1.5f;  //Assigns the float 1.5 to number 1
	private float number2 = 24.3f; //Assigns the float 24.3 to number 2
	
	//Getters and Setters for number 1 and 2
	public float getNumber1() {
		return number1;
	}
	public void setNumber1(float number1) {
		this.number1 = number1;
	}
	public float getNumber2() {
		return number2;
	}
	public void setNumber2(float number2) {
		this.number2 = number2;
	}
}
