public class Employee {
	String name;
	String department;
	int age;
	
	/**
	 * 
	 * @param name the string name for constructor
	 * @param department the string department for constructor
	 * @param age the integer age for constructor
	 */
	public Employee(String name, String department, int age){
		this.name = name;
		this.department = department;
		this.age = age;
	}
}
